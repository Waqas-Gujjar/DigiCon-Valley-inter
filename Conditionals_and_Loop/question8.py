# Write a program to print all prime numbers between two given numbers.


number = int(input('Enter a number: '))
if number % 2 == 0 :
    print(f'{number} is a prime number')  
else:
   print(f'{number} is not a prime number')     

# output

# Enter a number: 2
# 2 is a prime number

# Enter a number: 3
# 3 is not a prime number