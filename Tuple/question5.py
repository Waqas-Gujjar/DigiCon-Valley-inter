# Write a program to count the occurrences of an element in a tuple.

number = int(input('enter a number: '))
tuple1 = (1, 2, 3,100,11,145,124,1,12,14,16,)
count_of_tuple = tuple1.count(number)

print(f"the count of occurrences {count_of_tuple}")

