# Write a program to convert hours to minutes and seconds.

hours = int(input('Enter a hours: '))

minutes = hours * 60
second = minutes * 3600

print (f"hours = {hours}")
print (f"minutes = {minutes}")
print (f"second = {second}")

# output
# Enter a hours: 20
# hours = 20
# minutes = 1200
# second = 4320000




