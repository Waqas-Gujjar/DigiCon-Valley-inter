# Write a program to calculate simple interest.


principle = float(input('Enter a valu of Principal: '))
rate_of_interest = float(input('Enter a value of rate: '))
time = float(input('Enter a time value: '))

simple_interest = principle * rate_of_interest * time

print(f"the totle simple interest value is {simple_interest}")

