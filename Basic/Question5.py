# Write a program to find the modulus of two numbers.

dividend = int(input("Enter the dividend: "))
divisor = int(input("Enter the divisor: "))

modulus = dividend % divisor
print(f"The modulus of {dividend} and {divisor} is {modulus}.")

# output
# Enter the dividend: 10
# Enter the divisor: 30
# The modulus of 10 and 30 is 10.